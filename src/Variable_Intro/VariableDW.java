package Variable_Intro;

// Variable Lesson
public class VariableDW {
    public static void main(String[] args) {

        // Primitive Data Type
        short myAge = 25;
        String name = "Name";
        double price = 1.25;
        float avg = 1.5F;   // F or l
        char gender = 'F';
        long cash = 12345678L;  // L or l

        // Wrapper Class
        Short myNewAge = 12;
        Character newGender = 'A';
        Double pPrice = 2.9;
        Float ff = 2.4F;
        Byte _byte = 2;
        double result = myNewAge.doubleValue();
        System.out.println(myAge);
    }
}
